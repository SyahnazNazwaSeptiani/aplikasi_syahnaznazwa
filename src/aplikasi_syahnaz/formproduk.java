    private void tblinputbarangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblinputbarangMouseClicked
        try{
    int baris=tblinputbarang.getSelectedRow();
    String tabelKlik=(tblinputbarang.getModel().getValueAt(baris, 0).toString());
    String sql="select * from produk where ProdukID='"+tabelKlik+"'";
    pst=konek.prepareStatement(sql);
    rst=pst.executeQuery();
    if (rst.next()) {
        String ProdukID=rst.getString(("ProdukID"));
        txtidproduk1.setText(ProdukID);
        String NamaProduk=rst.getString(("NamaProduk"));
        txtnamaproduk.setText(NamaProduk);
        String Harga=rst.getString(("Harga"));
        txtharga.setText(Harga);
        String Stok=rst.getString(("Stok"));
        txtstok.setText(Stok);
    }
}catch (Exception e) {
   JOptionPane.showMessageDialog(null, e);

}        // TODO add your handling code here:
    }//GEN-LAST:event_tblinputbarangMouseClicked

